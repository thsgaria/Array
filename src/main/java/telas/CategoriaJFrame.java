    private void listarCategorias() {
            try {
            var jdcbUrl = "jdcb:mysql://localhost:3306/locadora";
            var jdcbUsuario = "root";
            var jdcbSenha = "admin";
            var conexao = DriverManager.getConnection(jdcbUrl, jdcbUsuario, jdcbSenha);
            var query = "SELECT id, nome FROM categoria";
            var statement = conexao.createStatement();
            var dados = statement.executeQuery(query);
            var modeloTabela = (DefaultTableModel) jTableCategoria.getModel();
            modeloTabela.setRowCount(0);
            while (dados.next()){
                var id = Integer.parseInt(dados.getString("id"));
                var nome = dados.getString("nome");
                modeloTabela.addRow(new Object[]{
                    id, nome
                });
            }
            } catch (Exception e) {
                e.printStackTrace();
            }
    }
    
    
    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
    var indiceLinhaSelecionada = jTableCategoria.getSelectedRow();
    idEscolhido = Integer.parseInt(jTableCategoria.getValueAt(indiceLinhaSelecionada,0).toString());
    var nomeEscolhido = jTableCategoria.getValueAt(indiceLinhaSelecionada, 1).toString();
    jTextFieldNome.setText(nomeEscolhido);
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        try {
            String nome = jTextFieldNome.getText();
            var jbdcUrl = "jdbc:mysql://localhost:3306/locadora";
            var jdbcUsuario = "root";
            var jdbcSenha = "admin";
            var conexao = DriverManager.getConnection(jbdcUrl, jdbcUsuario, jdbcSenha);
            String query;
            if (idEscolhido == -1) {
                query = "INSERT INTO categorias (nome) VALUES ('" + nome + "')";
            }else {
                query = "UPDATE categoria SET nome = '" + nome + "'WHERE id = " + idEscolhido;
                idEscolhido = -1;
            }
            var statement = conexao.createStatement();
            statement.execute(query);
            listarCategorias();
            JOptionPane.showMessageDialog(null, "Categoria cadastrada com sucesso");
            JOptionPane.showMessageDialog(null, nome);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

    private void jButtonApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonApagarActionPerformed
        try {
            var jdbcUrl = "jdbc:mysql://localhost:3306/locadora";
            var jdbcUsuario = "root";
            var jdbcSenha = "admin";
            
            var conexao = DriverManager.getConnection(jdbcUrl, jdbcUsuario, jdbcSenha);
            var indiceLinhaSelecionada = jTableCategoria.getSelectedRow();
            var idEscolhidoParaApagar = Integer.parseInt(jTableCategoria.getValueAt(indiceLinhaSelecionada, 0).toString());
            var query = "DELETE FROM categorias WHERE id = '" + idEscolhidoParaApagar + "'";
            var statement = conexao.createStatement();
            statement.execute(query);
            listarCategorias();
            JOptionPane.showMessageDialog(null, "Categoria apagada com sucesso");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButtonApagarActionPerformed

